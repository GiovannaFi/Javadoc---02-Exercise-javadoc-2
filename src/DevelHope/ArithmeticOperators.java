package DevelHope;

/**
 * this class contains the methods of the operations
 * @author Giovanna Fiaccabrino
 */
public class ArithmeticOperators {
    /**
     * @param num1(input)
     * @param num2(input)
     * @return returns their summation int
     */
    public int sum(int num1, int num2){
        return num1 + num2;
    }
    /**
     * @param num1(input)
     * @param num2(input)
     * @return returns their subtraction int
     */
    public int sub(int num1, int num2){
        return num1 - num2;
    }
    /**
     * @param num1(input)
     * @param num2(input)
     * @return returns their multiplication int
     */
    public int mul(int num1, int num2){
        return num1 * num2;
    }
    /**
     * @param num1(input)
     * @param num2(input)
     * @return returns their division int
     */
    public int div(int num1, int num2){
        return num1 / num2;
    }
}
